function renderUser(user) {
    let btn = helper('button', 'btn', 'Remove')
    let card = helper('div', 'card', '')
    let id = helper('p', 'id', user.id)
    let name = helper('h1', '', user.name)
    let username = helper('h2', '', user.username)
    let email = helper('a', '', user.email)
    let address = helper('p', '', user.address.street)
    let phone = helper('p', '', user.phone)
    let website = helper('p', '', user.website)
    let company = helper('p', '', user.company.name)


    card.append(btn)
    card.append(id)
    card.append(name)
    card.append(username)
    card.append(email)
    card.append(address)
    card.append(phone)
    card.append(website)
    card.append(company)

    removeCard(card)

    return card
}

function renderUsers(users) {

    let container = document.createElement('div')
    let btn = document.createElement('button')
    btn.classList.add('btn')

    btn.innerHTML = 'Remove'
    container.classList = 'container'
    users.forEach(user => {
        renderUser(user)
        container.append(renderUser(user))

    })


    return container

}

function createNewObj(form) {

    form.addEventListener('submit', e => {
        e.preventDefault()
        let user = {
            id: qvValue('.id'),
            name: qvValue('.name'),
            username: qvValue('.username'),
            email: qvValue('.email'),
            address: {
                street: qvValue('.address'),
            },
            phone: qvValue('.phone'),
            website: qvValue('.website'),
            company: {
                name: qvValue('.company'),
            },

        }


        document.body.querySelector('.container').append(renderUser(user))


    })
}
