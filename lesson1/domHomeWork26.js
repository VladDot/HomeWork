function helper(tag, classList = null, text = null, path = null, ) {
    const name = createHelper(tag)
    name.classList = classList
    name.innerHTML = text
    console.log(tag);
    /*  path.append(tag) */
    return name
}

function qvValue(select) {
    return document.querySelector(select).value
}

function createHelper(create) {
    return document.createElement(create)
}

