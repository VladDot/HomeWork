function removeCard(card) {

    card.addEventListener('click', e => {


        if (e.target.classList.value === 'card cardClick' ||
            e.target.parentElement.classList.value === 'card cardClick') {
            card.classList.remove('cardClick')

        } else if (e.target.classList.value === 'card' ||
            e.target.parentElement.classList.value === 'card'
        ) {

            card.classList.add('cardClick')
        }
        if (e.target.classList.value === 'btn') {

            card.remove()

        }
    })

}


